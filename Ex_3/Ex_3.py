### Exercício 2
# Escreva um script que leia três números e mostre o maior e o menor deles.

print("Digite os 3 números: ")
n1 = float(input())
n2 = float(input())
n3 = float(input())

números = [n1, n2, n3]
M = []
for x in números:
    M.append(x)
print(M)

M = sorted(M)
print(f"Numeros ordenados: {M}")

print(f"Maior: {M[-1]}")


